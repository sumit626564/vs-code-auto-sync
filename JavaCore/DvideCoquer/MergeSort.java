package JavaCore.DvideCoquer;

public class MergeSort {

    public static void printArr(int arr[]){
        for(int i=0; i<arr.length; i++){
            System.out.println(arr[i]+ " ");
        }
        System.out.println();
    }
    public static void main(String[] args) {
        int arr[] = {6, 3, 9, 5, 2, 8};
        printArr(arr);
    }
}
